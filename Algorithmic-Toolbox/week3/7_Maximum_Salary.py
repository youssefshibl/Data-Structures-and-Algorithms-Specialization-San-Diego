def Maximum_Salary(a):
    YourSalary = ""
    zeros = [0] * len(a)
    b = len(a)
    while b > 0:
        max_number = float("-inf")
        last_choose = 0 
        for i in range(len(a)):
            if(get_max_first_digit(a[i] , max_number) and zeros[i] == 0):
                max_number = a[i] 
                last_choose = i 

        YourSalary += str(max_number);  
        zeros[last_choose] = 1      
        b -=1
    return YourSalary;    




