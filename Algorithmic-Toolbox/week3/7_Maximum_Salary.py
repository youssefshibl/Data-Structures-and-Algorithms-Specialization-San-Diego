def get_max_first_digit(one , two):
   if(two == float("-inf")):
       return True
   one_new = int(str(one)[0])
   two_new = int(str(two)[0])
   if(one_new == two_new):
        one_new = int(str(one)[len(str(one))-1])
        two_new= int(str(two)[len(str(two))-1])
   return one_new > two_new


