def Optimal(n):
    summands = []
    p = 1
    while n > 0:
        n -= p
        if (p + 1) > n:
            last = p + n
            summands.append(last)
            n = 0
        else:
            summands.append(p)
            p += 1
    return summands


n = int(input())

summands = Optimal(n)

print(len(summands))
for x in summands:
    print(x, end=' ')

