def get_best_item(items):
    max_value = 0
    best_item =0
    n = len(items)
    for i in range(0 , n):
        if(items[i][0] > 0):
            if ((items[i][1] / items[i][0]) > max_value):
                best_item = i
                max_value =(items[i][1] / items[i][0])
    return best_item             

def knaspsack(W , items):
    n= len(items)
    amount = [0] * n
    total_values = 0
    for i in range(0, n):
        best = get_best_item(items)
        if(W > items[best][0]):
            a = items[best][0];
        else:
            a = W 
        total_values += a * (items[best][1] / items[best][0]) 
        items[best] = list(items[best])  
        items[best][0] = items[best][0] - a
        items[best] = tuple(items[best])
        amount[best] = amount[best] + a
        W -= a 
        if(W <= 0):
            break
        
    return total_values     

