def sorting_tuples(tuples ,type):
    n = len(tuples)
    my_zeros_array = [0] * n
    sorted = [0] * n
    for i in range(0,n) : 
        current = float("-inf");
        for j in range(0 , n) : 
            if(tuples[j][0] > current and my_zeros_array[j] == 0):
                index = j
                current = tuples[j][0]
        my_zeros_array[index] = 1
        sorted[i] =tuples[index]



    if(type == True):
        reverse_array = [0]*n
        for i in range(0,n):
            reverse_array[i]= sorted[n-1-i] 
        return reverse_array    

    else:
        return sorted 


    
