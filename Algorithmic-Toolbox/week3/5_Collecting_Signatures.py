def collecting_signatures(signatures):
    n = len(signatures)
    sorted_signatures = sorting_tuples(signatures,True)
    end = sorted_signatures[0][1]
    points = []
    for i in range(n):
        if(sorted_signatures[i][0] > end):
            points.append(end)
            end = sorted_signatures[i][1]
        else:
            end = min(end,sorted_signatures[i][1])    
    points.append(end)
    print(len(points));
    for i in range(len(points)):
        print(points[i], end=" ")


