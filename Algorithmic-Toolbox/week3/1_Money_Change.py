def money_change(money):
    number_of_coins = 0
    while money > 0 :
        if(money >= 10):
            money -= 10
        elif(money >=5):
            money -= 5
        else :
            money-=1
        number_of_coins+=1            
    return number_of_coins


money = int(input())
print(money_change(money))