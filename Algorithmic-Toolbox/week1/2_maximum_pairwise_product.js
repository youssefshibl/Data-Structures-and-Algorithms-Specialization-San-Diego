function MyCode(line, r1) {
  let n = parseInt(line);
  r1.on("line", (line) => {
    let arr = line
      .split(" ")
      .slice(0, n)
      .map((x) => parseInt(x));
    console.log(maxPairwiseProduct(n, arr));
    r1.close();
  });
}

function maxPairwiseProduct(n, arr) {
  let index1 = -1;
  for (let i = 0; i < n; i++) {
    if (index1 == -1 || arr[i] > arr[index1]) {
      index1 = i;
    }
  }
  let index2 = -1;
  for (let i = 0; i < n; i++) {
    if (i != index1 && (index2 == -1 || arr[i] > arr[index2])) {
      index2 = i;
    }
  }
  return arr[index1] * arr[index2];
}
