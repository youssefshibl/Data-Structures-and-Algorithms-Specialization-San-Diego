n = int(input())
a = [int(x) for x in input().split()]

index1 = -1
for i in range(0, n):

    if index1 == -1 or a[i] > a[index1]:
        index1 = i

index2 = -1
for i in range(0, n):
    if i != index1 and (index2 == -1 or a[i] > a[index2]):
        index2 = i

result = a[index1] * a[index2]
print(result)
