const readline = require("readline");
function RunAfterGetData(Run) {
  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout,
  });

  rl.on("line", (line) => {
    Run(line, rl);
  });
}
RunAfterGetData(MyCode);

// my code start from here
function MyCode(line, r1) {
  [a, b] = line.split(" ");
  console.log(+a + +b);
  r1.close;
}

