def get_fibonacci_number(number):
    if(number <= 1):
        return number
    fibonacci_array = [0, 1]
    for i in range(2, number + 1):
        fibonacci_array.append(fibonacci_array[i - 1] + fibonacci_array[i - 2])

    return fibonacci_array[number]


number = int(input())

print(get_fibonacci_number(number))
