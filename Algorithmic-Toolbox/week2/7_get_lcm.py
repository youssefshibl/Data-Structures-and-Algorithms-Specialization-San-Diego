# to understand the algorithm, you need to understand the concept of the greatest common divisor (GCD)
# and understand what is lcm go ot "https://www.khanacademy.org/math/cc-sixth-grade-math/cc-6th-expressions-and-variables/cc-6th-lcm/a/least-common-multiple-review"
# lcm(a,b) = a*b/gcd(a,b)

def gcd(a, b):
    if b == 0:
        return a
    a = a % b
    return gcd(b, a)

a, b = map(int, input().split())
print((a * b) // gcd(a, b))





