const readline = require("readline");
function RunAfterGetData(Run) {
  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout,
  });

  rl.on("line", (line) => {
    Run(line, rl);
  });
}
RunAfterGetData(MyCode);

// my code start from here
function MyCode(line, r1) {
  let [n, m] = line.split(" ");
  console.log(fibonacci(n, m));
  r1.close;
}

function fibonacci(number, m) {
    
  if (number <= 1) {
    return number;
  }
  let fibonacci_array = [0, 1];
  let period = 0;
  for (let i = 2; i < m * 20; i++) {
    fibonacci_array[i] = (fibonacci_array[i - 1] + fibonacci_array[i - 2]) % m;
    period++;
    if (fibonacci_array[i] == 1 && fibonacci_array[i - 1] == 0) {
      break;
    }
  }
  //console.log(fibonacci_array[number]);
  return fibonacci_array[number % period];
}
