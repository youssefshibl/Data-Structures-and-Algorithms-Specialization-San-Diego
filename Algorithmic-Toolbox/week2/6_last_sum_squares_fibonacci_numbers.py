# key of solution will be from 
#   F1^2 + F2^2 + ... Fn^2 = Fn * (Fn + Fn-1)
#  show at  https://i.stack.imgur.com/3DxbY.jpg
# last digit ld Fn_sum_sq_ld = Fn_ld * (Fn_ld + Fn-1_ld)

def get_fibonacci_huge_mod(n, m):
    if n <= 1:
        return n

    remainders = [0, 1]

    period = 0

    for i in range(2, 10*m):
        remainders.append((remainders[i - 1] + remainders[i - 2]) % m)
        period += 1
        if remainders[i - 1] == 0 and remainders[i] == 1:
            break
    return remainders[n % period]



    

# get n and m from user
n = int(input())

fn1_d1 = get_fibonacci_huge_mod(n , 10)
fn2_d1 = get_fibonacci_huge_mod(n-1, 10)

print((fn1_d1 * (fn1_d1 + fn2_d1)) % 10)

