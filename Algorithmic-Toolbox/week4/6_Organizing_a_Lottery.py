# slow solution
def oraganizing_lottery(segments, points):
    segments.sort(key=lambda x: x[0])
    points.sort()
    result = [0] * len(points)
    for i in range(len(points)):
        for j in range(len(segments)):
            if segments[j][0] <= points[i] <= segments[j][1]:
                result[i] += 1
            elif points[i] < segments[j][0]:
                break
    return result


# fast solution
def oraganizing_lottery_fast(points, segments):
    start_segments = [s[0] for s in segments]
    end_segments = [s[1] for s in segments]

    # Combine the points and segments into a single list
    events = [(point, 'p') for point in points]
    events += [(start, 's') for start in start_segments]
    events += [(end, 'e') for end in end_segments]

    # Sort the events by point position, breaking ties with start/end events
    events.sort(key=lambda e: (e[0], e[1]))

    count = 0
    result = []
    for point, event_type in events:
        if event_type == 's':
            count += 1
        elif event_type == 'e':
            count -= 1
        else:
            result.append(count)

    points_sorted = sorted(points)
    result_sorted = [result[points.index(p)] for p in points_sorted]
    return result_sorted

        

    


a, b = map(int, input().split())
segments = []
for i in range(0,a):
    segments.append(tuple(map(int, input().split())))
points = list(map(int, input().split()))[:b]   


print(*oraganizing_lottery_fast(points,segments))


