import random

def partition3(a, l, r):

    x = a[l]
    j = l
    k = l

    for i in range(l + 1, r + 1):
        if a[i] < x:
            j += 1
            k += 1
            a[i], a[j] = a[j], a[i]
            if j != k:
                a[i], a[k] = a[k], a[i]
        elif a[i] == x:
            k += 1
            a[i], a[k] = a[k], a[i]

    a[l], a[j] = a[j], a[l]

    return j, k


def randomized_quick_sort(a, l, r):

        if l >= r:
            return
    
        k = random.randint(l, r)
        a[l], a[k] = a[k], a[l]
    
        m1, m2 = partition3(a, l, r)
    
        randomized_quick_sort(a, l, m1 - 1)
        randomized_quick_sort(a, m2 + 1, r)

        return a



n = int(input())
target_array = [int(x) for x in input().split()][:n]
print(*randomized_quick_sort(target_array, 0, len(target_array) - 1))        

