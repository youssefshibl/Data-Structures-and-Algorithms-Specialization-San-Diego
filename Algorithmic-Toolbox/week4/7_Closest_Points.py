def closest_points(points):
    
    def distance(point1, point2):
        return ((point1[0] - point2[0])**2 + (point1[1] - point2[1])**2)**0.5
    
    def brute_force(points):
        min_distance = float('inf')
        for i in range(len(points)):
            for j in range(i+1, len(points)):
                min_distance = min(min_distance, distance(points[i], points[j]))
        return min_distance
    
    def closest_split_pair(points, d):
        x_bar = points[len(points)//2][0]
        s_y = sorted(points, key=lambda x: x[1])
        s_y_bar = []
        for i in range(len(s_y)):
            if abs(s_y[i][0] - x_bar) < d:
                s_y_bar.append(s_y[i])
        best = d
        for i in range(len(s_y_bar)):
            for j in range(1, min(7, len(s_y_bar) - i)):
                best = min(best, distance(s_y_bar[i], s_y_bar[i+j]))
        return best
    
    def closest_points_rec(points):
        if len(points) <= 3:
            return brute_force(points)
        mid = len(points) // 2
        left = points[:mid]
        right = points[mid:]
        d_left = closest_points_rec(left)
        d_right = closest_points_rec(right)
        d = min(d_left, d_right)
        d_split = closest_split_pair(points, d)
        return min(d, d_split)
    
    points.sort()
    return closest_points_rec(points)


n = int(input())
points = []
for i in range(n):
    points.append(tuple(map(int, input().split())))

print(closest_points(points))
