def selection_sort(target_array):
    n = len(target_array)
    for i in range(n):
        min_index = i 
        for j in range(i+1, n):
            if(target_array[j] < target_array[min_index]):
                min_index = j
        min_value = target_array[min_index]
        target_array[min_index]= target_array[i]
        target_array[i]= min_value
    return target_array            



print(selection_sort([5,6,9,7,4,1,2,3]))
