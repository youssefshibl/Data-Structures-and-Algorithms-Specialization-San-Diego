def get_majority_element(target):
    target.sort()
    count = 1
    for i in range(1, len(target)):
        if target[i] == target[i - 1]:
            count += 1
            if count > len(target) // 2:
                return 1
        else:
            count = 1
    return 0

array_length = int(input())
target_array = [int(x) for x in input().split()][:array_length]
print(get_majority_element(target_array))

