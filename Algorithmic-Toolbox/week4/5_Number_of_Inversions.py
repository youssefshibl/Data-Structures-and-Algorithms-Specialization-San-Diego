def number_of_inversions(arr):
    if len(arr) <= 1:
        return arr , 0

    mid = len(arr) // 2
    left = arr[:mid]
    right = arr[mid:]

    left_sorted , count_left = number_of_inversions(left)
    right_sorted , count_right = number_of_inversions(right)
    arr_sorted , count_merge = merge(left_sorted, right_sorted)

    return arr_sorted , count_left + count_right + count_merge 


def merge(left, right):
    result = []
    i = j = 0
    count = 0
    

    while i < len(left) and j < len(right):
        if left[i] <= right[j]:
            result.append(left[i])
            i += 1
        else:
            result.append(right[j])
            j += 1
            count += len(left[i:])

    result += left[i:]
    result += right[j:]

    return result , count


n = int(input())
target_array = [int(x) for x in input().split()][:n]
print(number_of_inversions(target_array)[1])
